public class Swicth 
{
	public static void main(String[] args) {
		char ch='a';
		switch(ch) {
		case 'A':
			System.out.println("A is Character");
		case 'Q':
			System.out.println("Q is Character");
		case 'a':
			System.out.println("a is Character");
			default:
				System.out.println("This all character are A or Q or a");
		}
	}
}
