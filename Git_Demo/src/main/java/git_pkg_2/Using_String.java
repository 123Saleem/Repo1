package git_pkg_2;

public class Using_String 
{
	public static void main(String[] args) {
		String course="Java";
		
		switch(course) {
		case "Python":
			System.out.println("Learn Python Language");
			break;
		case "c++":
			System.out.println("Learn C++ Language");
			break;
		case "Java":
			System.out.println("Learn Java Language");
			break;
		default:
			System.out.println("don't have languages");
			
		}
	}
}
